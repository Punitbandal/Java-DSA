import java.util.Scanner;

public class ProductOfTwoNum {

    public static int ProductOfTwoNum() {
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter The Numbers");
        int a = sc.nextInt();
        int b = sc.nextInt();

        return a * b;

    }

}
